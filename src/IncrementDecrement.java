public class IncrementDecrement {

    public static void main(String[] args) {


        int i = 10;

        System.out.println(i);  // 10

        System.out.println(i++); // 10

        System.out.println(++i); // 12

        System.out.println(i); // 12


        int j = 7;

        System.out.println(--j); //6

        System.out.println(j--); //6

        System.out.println(j); // 5

        System.out.println(j++); // 5

    }

}
